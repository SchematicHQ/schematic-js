// Generates src/cacheVersion.ts containing an 8-char hex hash derived from the
// shape of the types used in the persistent flag state cache. When any of
// these types change (fields added/removed/renamed/retyped), the hash changes,
// automatically invalidating cached data on user devices without manual
// version bumps.
//
// Mirrors the approach in rulesengine/version.go, which uses Go reflection to
// fingerprint cached struct types. TS types are erased at runtime, so we run
// this at build time via the TypeScript Compiler API.

import { createHash } from "node:crypto";
import { writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import ts from "typescript";

const here = dirname(fileURLToPath(import.meta.url));
const projectRoot = resolve(here, "..");
const entryPoint = resolve(projectRoot, "src/index.ts");
const outFile = resolve(projectRoot, "src/cacheVersion.ts");

// The types whose shape determines whether a previously-persisted cache is
// safe to read. Adding a new type to the cache payload? Add it here.
const targetTypeNames = [
  "CachedFlagState",
  "CachedContextEntry",
  "CheckFlagReturn",
  "CheckPlanReturn",
];

const program = ts.createProgram([entryPoint], {
  target: ts.ScriptTarget.ES2020,
  module: ts.ModuleKind.ESNext,
  moduleResolution: ts.ModuleResolutionKind.Bundler,
  esModuleInterop: true,
  strict: true,
  skipLibCheck: true,
});

const checker = program.getTypeChecker();

function findType(name) {
  for (const sourceFile of program.getSourceFiles()) {
    if (sourceFile.isDeclarationFile) continue;
    let found;
    ts.forEachChild(sourceFile, (node) => {
      if (found) return;
      if (
        (ts.isTypeAliasDeclaration(node) || ts.isInterfaceDeclaration(node)) &&
        node.name.text === name
      ) {
        found = checker.getTypeAtLocation(node.name);
      }
    });
    if (found) return found;
  }
  throw new Error(`type not found in program: ${name}`);
}

// Produce a deterministic structural fingerprint of a TS type by walking
// properties recursively. Sorted property names, optionality marker, and
// recursive expansion of object property types.
const MAX_DEPTH = 8;

function fingerprint(type, seen, depth) {
  if (depth > MAX_DEPTH) return "<max-depth>";

  const id = type.id ?? checker.typeToString(type);
  if (seen.has(id)) return `<cycle:${id}>`;
  seen.add(id);

  // Primitives, unions, literals, etc. — typeToString gives a stable repr.
  const isObject = (type.flags & ts.TypeFlags.Object) !== 0;
  if (!isObject) {
    seen.delete(id);
    return checker.typeToString(
      type,
      undefined,
      ts.TypeFormatFlags.NoTruncation,
    );
  }

  const props = checker.getPropertiesOfType(type);
  if (props.length === 0) {
    seen.delete(id);
    return checker.typeToString(
      type,
      undefined,
      ts.TypeFormatFlags.NoTruncation,
    );
  }

  const parts = [];
  for (const prop of [...props].sort((a, b) => a.name.localeCompare(b.name))) {
    const decl = prop.valueDeclaration ?? prop.declarations?.[0];
    if (!decl) continue;
    const propType = checker.getTypeOfSymbolAtLocation(prop, decl);
    const optional = (prop.flags & ts.SymbolFlags.Optional) !== 0 ? "?" : "";
    parts.push(`${prop.name}${optional}:${fingerprint(propType, seen, depth + 1)}`);
  }

  seen.delete(id);
  return `{${parts.join(";")}}`;
}

const hasher = createHash("sha256");
for (const name of targetTypeNames) {
  const type = findType(name);
  hasher.update(name);
  hasher.update("=");
  hasher.update(fingerprint(type, new Set(), 0));
  hasher.update("\n");
}
const cacheVersion = hasher.digest("hex").slice(0, 8);

const banner = `// AUTO-GENERATED by scripts/generate-cache-version.mjs. Do not edit by hand.
// Hash of the structural shape of cached types; bumps automatically when any
// of CachedFlagState, CachedContextEntry, CheckFlagReturn, or CheckPlanReturn
// changes shape. See scripts/generate-cache-version.mjs for details.
`;

writeFileSync(
  outFile,
  `${banner}export const cacheVersion = "${cacheVersion}";\n`,
);

console.log(`generated ${outFile} with cacheVersion=${cacheVersion}`);
